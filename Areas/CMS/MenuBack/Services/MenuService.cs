        #region Importations
        public List<Menu> ImportExcel(string path, int userId)
        {
            List<Menu> lstMenu = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string Name = row.Cell(7).GetString();
                    int MenuFatherId = Convert.ToInt32(row.Cell(8).GetString());
                    int Order = Convert.ToInt32(row.Cell(9).GetString());
                    string URLPath = row.Cell(10).GetString();
                    string IconURLPath = row.Cell(11).GetString();


                    Menu Menu = new()
                    {
                        MenuId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Name = Name,
                        MenuFatherId = MenuFatherId,
                        Order = Order,
                        URLPath = URLPath,
                        IconURLPath = IconURLPath,

                    };

                    lstMenu.Add(Menu);
                }
            }

            return lstMenu;
        }
